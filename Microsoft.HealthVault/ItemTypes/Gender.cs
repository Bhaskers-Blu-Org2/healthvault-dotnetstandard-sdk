namespace Microsoft.HealthVault.ItemTypes
{
    /// <summary>
    /// Provides values representing a person's gender.
    /// </summary>
    ///
    public enum Gender
    {
        /// <summary>
        /// The gender is unknown or has a value that is not understood by
        /// this client.
        /// </summary>
        ///
        Unknown = 0,

        /// <summary>
        /// The person is male.
        /// </summary>
        ///
        Male = 1,

        /// <summary>
        /// The person is female.
        /// </summary>
        ///
        Female = 2
    }
}
